// 5. Crie uma função que exiba a quantidade de frutas em um array de frutas

function exibirQuantidadeDeFrutas(frutas) {
  let quantidadeFrutas = frutas.length;
  console.log("Quantidade de frutas:", quantidadeFrutas);
}

let frutas = ["maçã", "banana", "laranja", "uva"];
exibirQuantidadeDeFrutas(frutas);
